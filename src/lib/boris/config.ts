/**
 * Конфигурация Action-Бориса (Спринт 7.16.D).
 *
 * Все значения читаются из env при импорте модуля. Менять прямо в runtime
 * нельзя — для A/B-теста перезапусти процесс с другим env.
 *
 * Default'ы подобраны под текущий профиль использования (≤10 turn'ов в одной
 * беседе, личный разговор в TG). Если Action-Борис начнёт обслуживать команду
 * параллельно — окно можно увеличить через env.
 */

/**
 * Сколько последних BorisMessage передавать модели как контекст истории.
 *
 * Меньше — дешевле и быстрее, но Боря теряет память «о чём говорили 30 минут
 * назад». 20 messages ≈ 10 turn'ов user/assistant — комфортный диалог.
 *
 * Безопасное обрезание (orphan tool_result на границе) делает
 * clipConversationWindow в @/lib/llm/conversation-window.
 */
export const BORIS_HISTORY_WINDOW = Number(
  process.env.BORIS_HISTORY_WINDOW ?? 20,
)

/**
 * Сколько минут conversation считается активной с момента последнего
 * сообщения. Превышение → conversation auto-closed при следующем обращении
 * того же юзера, заводится новая. Это срезает линейный рост стоимости при
 * долгих перерывах в диалоге.
 *
 * 120 мин — типичная пауза «утро → обед» у менеджера; новая мысль обычно
 * не связана с прошлой темой, контекст хорошо бы сбросить.
 */
export const BORIS_CONVERSATION_TTL_MINUTES = Number(
  process.env.BORIS_CONVERSATION_TTL_MINUTES ?? 120,
)
